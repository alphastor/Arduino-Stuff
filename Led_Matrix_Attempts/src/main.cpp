void setup()
{
  Serial.begin(9600);

  for(int i = 0;i<8;i++)   // iterate over the pins
  {
    pinMode(Row[i],OUTPUT);  // initialize the output pins
    pinMode(Col[i],OUTPUT);  // initialize the output pins
  }
}

void Clear()
{
  for(int i = 0;i<8;i++)
  {
    digitalWrite(Row[i],HIGH);
    digitalWrite(Col[i],LOW);
  }
}

void Display(unsigned char dat[8][8])
{
  for(int c = 0; c<8;c++)
  {
    digitalWrite(Col[c],HIGH); //use thr column

    for(int r = 0;r<8;r++)
    {
      digitalWrite(Row[r],dat[r][c]);
    }

    // delay(0.9999999);
    delay(1);
    Clear();  //LOW the display light
  }
}

